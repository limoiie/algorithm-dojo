TEST(TestPopulatingNextRightPointersInEachNode, testcase) {
    auto sol = Solution();

    auto cases = vector<tuple<Node*, Node*>>{
            {},
    };

    for (auto & c : cases) {
        auto result = sol.connect(get<0>(c));
        auto expect = get<1>(c);
        ASSERT_EQ(result, expect);
    }
}
