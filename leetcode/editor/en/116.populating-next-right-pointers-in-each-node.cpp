class Solution {
public:
    Node* connect(Node* root) {
        if (root == nullptr) return nullptr;
        connect(root->left, root->right);
        connect(root->left);
        connect(root->right);
        return root;
    }

    void connect(Node* left, Node* right) {
        while (left != nullptr && right != nullptr) {
            left->next = right;
            left = left->right;
            right = right->left;
        }
    }
};
